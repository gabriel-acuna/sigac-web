let evaluacionsSlice = createSlice({
    name: 'evaluacionesPersonal',
    initialState: {
        data: {
            evaluaciones: []
        },
        status: ''

    },
    reducers: {

        clearData: (state) => {
            state.data = {
                evaluaciones: []
               
            }
        }

    },
    extraReducers: {
        [loadEvaluacionesPersonal.fulfilled]: (state, action) => {
            state.status = 'success'
            state.data = {
                evaluaciones: action.payload
            }

        }


    }
}
)

export const { clearData } = evaluacionsSlice.actions;
export default evaluacionsSlice.reducer;