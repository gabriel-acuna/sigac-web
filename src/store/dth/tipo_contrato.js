let tiposContratosSlice = createSlice({
    name: 'tiposContratos',
    initialState: {
        data: {
            contratos: []
        },
        status: ''

    },
    reducers: {

        clearData: (state) => {
            state.data = {
                contratos: []
               
            }
        }

    },
    extraReducers: {
        [loadTiposContratos.fulfilled]: (state, action) => {
            state.status = 'success'
            state.data = {
                contratos: action.payload
            }

        }


    }
}
)

export const { clearData } = tiposContratosSlice.actions;
export default tiposContratosSlice.reducer;