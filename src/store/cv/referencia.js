let referenciasSlice = createSlice({
    name: 'referencias',
    initialState: {
        data: {
            referencias: []  
        },
        status: ''

    },
    reducers: {

        clearData: (state) => {
            state.data = {
                referencias: []
            }
        }

    },
    extraReducers: {
        [loadReferencias.fulfilled]: (state, action) => {
            state.status = 'success'
            state.data = {
                personal: action.payload
            }
        }


    }
}
)

export const { clearData } = referenciasSlice.actions;
export default referenciasSlice.reducer;