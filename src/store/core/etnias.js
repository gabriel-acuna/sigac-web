let etniasSlice = createSlice({
    name: 'etnias',
    initialState: {
        data: {
            etnias: [],
            etnia: null
        },
        status: ''

    },
    reducers: {

        clearData: (state) => {
            state.data = {
                etnias: []
               
            }
        }

    },
    extraReducers: {
        [loadEtnias.fulfilled]: (state, action) => {
            state.status = 'success'
            state.data = {
                    etnias: action.payload
                }

        }


    }
}
)

export const { clearData } = etniasSlice.actions;
export default etniasSlice.reducer;