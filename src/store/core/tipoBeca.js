let tipoBecasSlice = createSlice({
    name: 'tipoBecas',
    initialState: {
        data: {
            tipoBecas: []
        },
        status: ''

    },
    reducers: {

        clearData: (state) => {
            state.data = {
                tipoBecas: []
               
            }
        }

    },
    extraReducers: {
        [loadTipoBecas.fulfilled]: (state, action) => {
            state.status = 'success'
            state.data = {
                    tipoBecas: action.payload
                }

        }


    }
}
)

export const { clearData } = tipoBecasSlice.actions;
export default tipoBecasSlice.reducer;